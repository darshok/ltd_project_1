package Ejemplos.Basic;

public class Ejemplo_Bucles_1 {

    public static void main(String[] args) {
        System.out.println("Empieza bucle WHILE:");
        int x = 1;
        while (x<=10) {
            System.out.print(" " + x);
            x++;
        }
        System.out.println();
    }
}
